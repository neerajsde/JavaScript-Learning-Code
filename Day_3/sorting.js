let arr = [1, 8, 9, 2, 4, 10];

// Use a comparison function to sort numbers in ascending order
arr.sort((a, b) => a - b);

console.log(arr);

// descending Order Sort
arr.sort((a, b) => b - a);

console.log(arr);

